package model;

public class CustomerDTO {
	private String cust_id;
	private String cust_name;
	private String cust_gender;
	private String cust_type;
	private String cust_bdate;
	private String cust_phone;
	private String cust_job;
	private String cust_addr;
	public String getCust_id() {
		return cust_id;
	}
	public void setCust_id(String cust_id) {
		this.cust_id = cust_id;
	}
	public String getCust_name() {
		return cust_name;
	}
	public void setCust_name(String cust_name) {
		this.cust_name = cust_name;
	}
	public String getCust_gender() {
		return cust_gender;
	}
	public void setCust_gender(String cust_gender) {
		this.cust_gender = cust_gender;
	}
	public String getCust_type() {
		return cust_type;
	}
	public void setCust_type(String cust_type) {
		this.cust_type = cust_type;
	}
	public String getCust_bdate() {
		return cust_bdate;
	}
	public void setCust_bdate(String cust_bdate) {
		this.cust_bdate = cust_bdate;
	}
	public String getCust_phone() {
		return cust_phone;
	}
	public void setCust_phone(String cust_phone) {
		this.cust_phone = cust_phone;
	}
	public String getCust_job() {
		return cust_job;
	}
	public void setCust_job(String cust_job) {
		this.cust_job = cust_job;
	}
	public String getCust_addr() {
		return cust_addr;
	}
	public void setCust_addr(String cust_addr) {
		this.cust_addr = cust_addr;
	}
	
	
}
